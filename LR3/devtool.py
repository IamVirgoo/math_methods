import numpy as np
from sympy import *


def get_eigenvalues(coefs):
    n = len(coefs)
    x = Symbol('x')
    answer = x ** n
    for i in range(n):
        answer -= coefs[n - 1 - i] * x ** i
    return solve((-1) ** n * (answer), x)


